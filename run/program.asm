# calculate 6th triangle number (it's 21)
STORE CONST 0x06 REG_A # addr 0x00-0x01
STORE CONST 0x00 REG_B # addr 0x02-0x03
loop: STORE ALU ADD REG_B    # addr 0x04
STORE ALU DEC REG_A
STORE FLAG ALU CMP_EQZ REG_A
JMP_IF >loop
DISPLAY REG_B
HALT
